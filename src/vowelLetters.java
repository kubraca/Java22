public class vowelLetters {
    public static void main(String[] args) {
        char harf = 'G';
        switch (harf) {
            case 'A':
            case 'i':
            case 'O':
            case 'E':
            case 'U':
                System.out.println("Sesli harftir.");
                break;
            default:
                System.out.println("Sessiz harftir.");

        }
    }
}

